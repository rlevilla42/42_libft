#include <stdio.h>
#include <strings.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*ptrstr;

	i = 0;
	ptrstr = (char *)s;
	while (i < n)
	{
		ptrstr[i] = '\0';
		i++;
	}
}
